package com.rainbow.ircontrol;

public class IRConfig {
    private int fre;
    private String nickName;
    private String realmID;
    private int rid;
    private Key[] keys;

    public int getFre() {
        return fre;
    }

    public String getNickName() {
        return nickName;
    }

    public String getRealmID() {
        return realmID;
    }

    public int getRid() {
        return rid;
    }

    public Key[] getKeys() {
        return keys;
    }
}
